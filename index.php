			<h3 id="WFH">Kegiatan Selama WFH</h3>
            <p>WFH adalah singkatan dari <i>work from home</i> yang berarti bekerja dari rumah. 
                Secara umum biasanya <i>work from home</i> diartikan dengan cara kerja karyawan yang berada di luar kantor.
                Entah dari rumah, dari cafe atau restoran sesuai dengan keinginan karyawan. 
                Sistem kerja wfh memang memiliki fleksibilitas yang tinggi. 
                Hal ini guna mendukung keseimbangan karyawan antara pekerjaan dan kehidupan.  </p>
            <p>Istilah WFH baru <i>trending</i> sejak pandemic virus corona menyerah hampir seluruh negara di dunia. 
                Virus corona mengharukan kita untuk selalu beraktifitas di dalam kediaman masing-masing, guna mentolerir penularan. 
                dikarenakan belum adanya vaksin yang tepat untuk membasmi virus tersebut, maka hampir semua pemerintahan di setiap negara  
                menegaskan untuk melakukan WFH ini. </p>
            <p>untuk mengenang suasana WFH ini, banyak hal yang dilakukan orang-orang. Termasuk saya, kesempatan untuk melatih <i>skill-skill</i>
                bisa didapatkan, walaupun dalam keadaan terperangkap di rumah. Seperti bermain gitar, beberapa telah saya lewati untuk belajar cara 
                memainkan gitar. Belajar dari paling dasar, yaitu kunci dasar gitar dan dilanjut memainkan beberapa lagu. Saya pun berniat melakukan 
                <i>cover-cover</i>lagu, bersama dengan teman saya sebagai vokalisnya. 
